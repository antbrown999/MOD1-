function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a -b; 
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

function printHello(name) {
    console.log('Hello' + name)
}

console.log(add(4,6)); // 10
console.log(subtract(7,3)); // 4
console.log(multiply(8,2)); // 16
console.log(divide(6,2)); // 3
